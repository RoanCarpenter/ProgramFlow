public class ForLoops
{

    public void run() {
        for (int i = 0; i >= 15; i += 1) {
            System.out.println(+ i + "is less than or equal to 15");
        }

    }

    public static void main(String[] args)
    {        
        ForLoops hw = new ForLoops();
        hw.run();
    }
}
